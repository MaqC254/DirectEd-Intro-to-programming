#initializes the variable people to 20
people = 20
#initializes the variable cats to 30
cats = 30
#initializes the variable dogs to 15
dogs = 15

#checks if the value stored in the variable people is less than cats. If True the print statement runs
if people < cats:
    print("Too many cats! The world is doomed!")
#checks if the value stored in the variable people is greater than cats. If True the print statement runs
if people > cats:
    print("Not many cats! The world is saved!")
#checks if the value stored in the variable people is less than dogs. If True the print statement runs
if people < dogs:
    print("The world is drooled on!")
#checks if the value stored in the variable people is greater than dogs. If True the print statement runs
if people > dogs:
    print("The world is dry!")
#increments the value stored in dogs by 5
dogs += 5
#checks if the value stored in the variable people is greater than or equal to dogs. If True the print statement runs
if people >= dogs:
    print("People are greater than or equal to dogs.")
#checks if the value stored in the variable people is less than or equal to dogs. If True the print statement runs
if people <= dogs:
    print("People are less than or equal to dogs.")
#checks if the value stored in the variable people is equal to dogs. If True the print statement runs
if people == dogs:
    print("People are equal to dogs.")